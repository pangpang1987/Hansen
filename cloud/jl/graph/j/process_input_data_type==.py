#
#   多类型输入数据处理函数
#
def process_input_data_type(data: Union[str, List[str], None] = None) -> Union[List[str], None]:
    """
    统一输入为列表格式的函数
    
    参数:
        data: 字符串、字符串列表或None
    
    返回:
        总是返回字符串列表
        
    行为:
        - 输入为字符串: 返回包含该字符串的单元素列表
        - 输入为列表: 直接返回该列表
        - 输入为None: 调用func1()返回列表
    """
    if isinstance(data, str):
        # 字符串转换为单元素列表
        return [data]
    elif isinstance(data, list):
        # 直接返回列表
        return data
    elif data is None:
        # 调用func1获取默认列表
        # return func1()
        return None
    else:
        # raise TypeError("输入必须是字符串、字符串列表或None")
        raise TypeError("The input must be a string, a list of strings, or None.")

